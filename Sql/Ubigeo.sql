CREATE VIEW vw_ubigeos AS
SELECT 
	uc.id,
	d.nombre AS departamento,
	p.nombre AS provincia,
	di.nombre AS distrito
FROM ( 
	SELECT 
		u.id,
		u.nombre,
		substring(u.id, 1, 2)||'0000' AS departamento_id,
		CASE
		    WHEN substring(u.id, 3, 2) <> '00' THEN substring(u.id, 1, 4)||'00'
		    ELSE NULL
		END AS provincia_id,
		CASE
		    WHEN substring(u.id, 5, 2) <> '00' THEN u.id
		    ELSE NULL
		END AS distrito_id
	FROM 
		ubigeos u
) uc
LEFT JOIN ubigeos d ON uc.departamento_id = d.id
LEFT JOIN ubigeos p ON uc.provincia_id = p.id
LEFT JOIN ubigeos di ON uc.distrito_id = di.id;
